class RoesslerWord3(diffrax.AbstractSolver[None]):
    """Depth-3 Heun/Roessler rough solver.

    This solver consumes the existing ``RoughTerm(solution="stratonovich")`` log
    coefficients. On each macro-step it converts the depth-3 Lyndon log signature
    to word signature tensors, applies the Heun3 rough RK step using true level-2
    channels, and adds the order-3 primitive correction from the same B-series
    equations for the active driver dimension.
    """

    term_structure: ClassVar = RoughTerm
    interpolation_cls: ClassVar = diffrax.LocalLinearInterpolation

    def order(self, terms):
        del terms
        return 3

    def strong_order(self, terms):
        del terms
        return None

    def init(self, terms, t0, t1, y0, args):
        del terms, t0, t1, y0, args
        return None

    def step(self, terms, t0, t1, y0, args, solver_state, made_jump):
        del args, solver_state, made_jump
        rough_term = unwrap_rough_term(terms)
        _validate_roessler_term(rough_term)

        if rough_term.depth3_word_control is None:
            log_coeffs = terms.contr(t0, t1)
            dx, x2, x3 = _signature_tensors_from_log_signature(
                log_coeffs,
                rough_term.basis.keys,
                rough_term.basis.dim,
            )
        else:
            dx, x2, x3 = rough_term.depth3_word_contr(t0, t1)
        y1 = _heun3_rough_step(rough_term.vector_field, y0, dx, x2, x3)

        return y1, None, dict(y0=y0, y1=y1), None, diffrax.RESULTS.successful

