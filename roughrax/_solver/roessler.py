def _frozen_b_value(index: int, trial: int = ROESSLER_FROZEN_B_TRIAL) -> float:
    modulus = 1009
    raw = ((index + 1) * (37 + 2 * trial) + (trial + 3) ** 3) % modulus
    centred = raw - modulus // 2
    if centred == 0:
        centred = trial + 1
    return centred / 37


